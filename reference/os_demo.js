const os = require('os');

// Platform
console.log(os.platform());

// CPU Architecture
console.log(os.arch());

// CPU Core Info
console.log(os.cpus());

// Memory Info
console.log(os.freemem());

// Total Memory
console.log(os.totalmem());

// Home Directory
console.log(os.homedir());

// Uptime
console.log(os.uptime());
